public class Bike 
{
     int id;
     String name;
     int cc;
    public Bike(int id, String name, int cc) 
    {
        this.id = id;
        this.name = name;
        this.cc = cc;
    }
  



    public String toString() {
        return "Bike [id=" + id + ", name=" + name + ", cc=" + cc + "]";
    }

  

}
